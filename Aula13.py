nome = 'Luiz Otávio'
altura = 1.80
peso = 95
imc = peso / altura ** 2 # IMC = peso / (altura x altura)

linha_1 = f'{nome} tem {altura:.2f} de altura'

linha_2 = f'quilos e seu IMC é {imc:.2f}'

print(linha_1, peso, linha_2)

